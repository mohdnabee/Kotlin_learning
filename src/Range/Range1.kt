package Range

fun main (){

    //  Kotlin Ranges
    //With the for loop, you can also create ranges of values with "..":
    //
    //Example
    //Print the whole alphabet:

    for (chars in 'a'..'x'){
        println(chars)
    }


    //  you can also create ranges of numbers:
    //  example
    for (nums in 5..15){
        println(nums)
    }

    //  Note: The first and last value is included in the range.
}